class ST7789:
    """ST7789 显示屏驱动：硬件控制 + 帧缓冲 + 字体绘制"""

    def __init__(self, spi_bus=1, spi_dev=0,
                 rst_chip="/dev/gpiochip0", rst_pin=19,
                 dc_chip="/dev/gpiochip0", dc_pin=21,
                 width=320, height=240, x_offset=0, y_offset=0,
                 speed_hz=1000000, spi_mode=3):
        # !!! rst_pin/dc_pin 须改成 物理29(RES)/物理31(DC) 实际对应的 gpiochip 线号 !!!
        #   默认：物理29 = GPIO0_C3 (2*8+3=19)，物理31 = GPIO0_C5 (2*8+5=21)
        self.width = width
        self.height = height
        self.x_offset = x_offset
        self.y_offset = y_offset

        # SPI 初始化
        self.spi = spidev.SpiDev()
        self.spi.open(spi_bus, spi_dev)
        self.spi.mode = spi_mode        # CPOL=1, CPHA=1（花屏可试 0）
        self.spi.max_speed_hz = speed_hz  # 先 1MHz 排查花屏；稳定后可调 5M/10M

        # GPIO 初始化（CS 由硬件控制，这里只需 RES 和 DC）
        self._rst_chip = gpiod.Chip(rst_chip)
        self._dc_chip = gpiod.Chip(dc_chip)
        self._rst_line = self._rst_chip.get_line(rst_pin)
        self._dc_line = self._dc_chip.get_line(dc_pin)
        self._rst_line.request(consumer="st7789", type=gpiod.LINE_REQ_DIR_OUT, default_vals=[1])
        self._dc_line.request(consumer="st7789", type=gpiod.LINE_REQ_DIR_OUT, default_vals=[0])

        # 帧缓冲
        self.fbuf = bytearray(width * height * 2)

    # ==================== 底层函数 ====================
    def _spi_write(self, data):
        """SPI 发送数据；CS 由 spidev 硬件自动控制，writebytes2 自动分片大缓冲"""
        self.spi.writebytes2(data)

    def _cmd(self, cmd):
        """发送命令"""
        self._dc_line.set_value(0)
        self._spi_write([cmd])

    def _data(self, data):
        """发送数据"""
        self._dc_line.set_value(1)
        if isinstance(data, list):
            self._spi_write(data)
        else:
            self._spi_write([data])

    def _reset(self):
        """屏幕复位"""
        self._rst_line.set_value(0)
        time.sleep(0.1)
        self._rst_line.set_value(1)
        time.sleep(0.15)

    # ==================== ST7789 初始化 ====================
    def init(self):
        self._reset()
        self._cmd(0x36)
        self._data(0x20)   # 横屏 320x240：镜像试 0x60/0xA0/0xE0
        self._cmd(0x3A)
        self._data(0x55)
        self._cmd(0xB2)
        self._data([0x0C, 0x0C, 0x00, 0x33, 0x33])
        self._cmd(0xB7)
        self._data(0x35)
        self._cmd(0xBB)
        self._data(0x19)
        self._cmd(0xC0)
        self._data(0x2C)
        self._cmd(0xC2)
        self._data(0x01)
        self._cmd(0xC3)
        self._data(0x12)
        self._cmd(0xC4)
        self._data(0x20)
        self._cmd(0xC6)
        self._data(0x0F)
        self._cmd(0xD0)
        self._data([0xA4, 0xA1])
        self._cmd(0xE0)
        self._data([0xD0,0x08,0x11,0x08,0x0C,0x15,0x39,0x44,0x4D,0x18,0x12,0x17,0x1A,0x19])
        self._cmd(0xE1)
        self._data([0xD0,0x08,0x10,0x08,0x06,0x06,0x39,0x44,0x4C,0x17,0x14,0x15,0x18,0x19])
        self._cmd(0x21)
        self._cmd(0x11)
        time.sleep(0.15)
        self._cmd(0x29)

    # ==================== 窗口 / 清屏 ====================
    def set_window(self, x0, y0, x1, y1):
        """设置显示区域，自动加偏移"""
        x0 += self.x_offset; x1 += self.x_offset
        y0 += self.y_offset; y1 += self.y_offset
        self._cmd(0x2A)
        self._data([x0 >> 8, x0 & 0xFF, x1 >> 8, x1 & 0xFF])
        self._cmd(0x2B)
        self._data([y0 >> 8, y0 & 0xFF, y1 >> 8, y1 & 0xFF])
        self._cmd(0x2C)

    def clear(self, color):
        """直接清屏，color: 16位RGB颜色"""
        self.set_window(0, 0, self.width - 1, self.height - 1)
        high = (color >> 8) & 0xFF
        low = color & 0xFF
        buf = bytearray([high, low]) * (self.width * self.height)
        self._dc_line.set_value(1)   # 关键：进入数据模式，否则像素会被当成命令丢弃
        self._spi_write(buf)

    # ==================== 帧缓冲绘图 ====================
    def draw_pixel(self, x, y, color):
        if 0 <= x < self.width and 0 <= y < self.height:
            i = (y * self.width + x) * 2
            self.fbuf[i]   = (color >> 8) & 0xFF
            self.fbuf[i+1] = color & 0xFF

    def fill_rect(self, x, y, w, h, color):
        x0 = max(0, x); y0 = max(0, y)
        x1 = min(self.width, x + w); y1 = min(self.height, y + h)
        for py in range(y0, y1):
            for px in range(x0, x1):
                self.draw_pixel(px, py, color)

    def fill_screen(self, color):
        hi = (color >> 8) & 0xFF
        lo = color & 0xFF
        for i in range(0, len(self.fbuf), 2):
            self.fbuf[i] = hi
            self.fbuf[i+1] = lo

    def flush(self):
        """将帧缓冲整屏刷新到屏幕"""
        self.set_window(0, 0, self.width - 1, self.height - 1)
        self._dc_line.set_value(1)
        self._spi_write(bytes(self.fbuf))

    # ==================== 字体绘制 ====================
    @staticmethod
    def _font_glyph(ch):
        """返回字符 ch 的 5 字节字形，不存在返回 None"""
        idx = ord(ch) - 32
        if 0 <= idx < 95:
            off = idx * 5
            return _FONT_DATA[off:off+5]
        return None

    def draw_char(self, x, y, ch, color, scale=1):
        glyph = self._font_glyph(ch)
        if glyph is None:
            return
        for col in range(5):
            bits = glyph[col]
            for row in range(8):
                if bits & (1 << (7 - row)):
                    for sx in range(scale):
                        for sy in range(scale):
                            self.draw_pixel(x + col*scale + sx, y + row*scale + sy, color)

    def draw_text(self, x, y, text, color, scale=1):
        for i, ch in enumerate(text):
            self.draw_char(x + i * 6 * scale, y, ch, color, scale)

    # ==================== 资源释放 ====================
    def close(self):
        self._dc_line.release()
        self._rst_line.release()
        self.spi.close()
        self._dc_chip.close()
        self._rst_chip.close()
